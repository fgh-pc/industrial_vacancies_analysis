def classify_industry_segment(vacancy_name: str, employer_name: str = '') -> str:
    """
    –ö–ª–∞—Å—Å–∏—Ñ–∏—Ü–∏—Ä—É–µ—Ç –≤–∞–∫–∞–Ω—Å–∏—é –ø–æ –æ—Ç—Ä–∞—Å–ª–µ–≤–æ–º—É —Å–µ–≥–º–µ–Ω—Ç—É.
    –£–ª—É—á—à–µ–Ω–Ω–∞—è –≤–µ—Ä—Å–∏—è —Å –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–∞–º–∏ –∏ –±–æ–ª–µ–µ —Ç–æ—á–Ω—ã–º —Å–æ–ø–æ—Å—Ç–∞–≤–ª–µ–Ω–∏–µ–º.
    
    Args:
        vacancy_name: –ù–∞–∑–≤–∞–Ω–∏–µ –≤–∞–∫–∞–Ω—Å–∏–∏
        employer_name: –ù–∞–∑–≤–∞–Ω–∏–µ —Ä–∞–±–æ—Ç–æ–¥–∞—Ç–µ–ª—è (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ)
        
    Returns:
        –ù–∞–∑–≤–∞–Ω–∏–µ –æ—Ç—Ä–∞—Å–ª–µ–≤–æ–≥–æ —Å–µ–≥–º–µ–Ω—Ç–∞ –∏–ª–∏ '–¥—Ä—É–≥–æ–µ'
    """
    name_lower = (vacancy_name or '').lower()
    employer_lower = (employer_name or '').lower()
    combined_text = f"{name_lower} {employer_lower}"
    
    # –°–ª–æ–≤–∞—Ä—å –¥–ª—è –ø–æ–¥—Å—á–µ—Ç–∞ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π –ø–æ –∫–∞–∂–¥–æ–º—É —Å–µ–≥–º–µ–Ω—Ç—É
    segment_scores = {}
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –ø–æ –≤—Å–µ–º –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º
    # –ò—Å–∫–ª—é—á–∞–µ–º '–¥—Ä—É–≥–∏–µ_–ø—Ä–æ–º—ã—à–ª–µ–Ω–Ω—ã–µ' –∏–∑ –æ—Å–Ω–æ–≤–Ω—ã—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π
    for segment, keywords in INDUSTRY_SEGMENTS_KEYWORDS.items():
        if segment == '–¥—Ä—É–≥–∏–µ_–ø—Ä–æ–º—ã—à–ª–µ–Ω–Ω—ã–µ':
            continue  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —ç—Ç—É –∫–∞—Ç–µ–≥–æ—Ä–∏—é –≤ –æ—Å–Ω–æ–≤–Ω–æ–º —Ü–∏–∫–ª–µ
        score = 0
        for keyword in keywords:
            # –¢–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å–ª–æ–≤–∞ (–∫–∞–∫ –æ—Ç–¥–µ–ª—å–Ω–æ–µ —Å–ª–æ–≤–æ) –¥–∞–µ—Ç –±–æ–ª—å—à–µ –±–∞–ª–ª–æ–≤
            keyword_clean = keyword.strip()
            if f' {keyword_clean} ' in f' {combined_text} ':
                score += 2  # –¢–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å–ª–æ–≤–∞
            elif keyword_clean in combined_text:
                score += 1  # –ß–∞—Å—Ç–∏—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ
        
        if score > 0:
            segment_scores[segment] = score
    
    # –ï—Å–ª–∏ –Ω–∞–π–¥–µ–Ω–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–µ–≥–º–µ–Ω—Ç —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–º —Å—á–µ—Ç–æ–º
    if segment_scores:
        return max(segment_scores.items(), key=lambda x: x[1])[0]
    
    # –ï—Å–ª–∏ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ, –ø—Ä–æ–≤–µ—Ä—è–µ–º –æ–±—â–∏–µ –ø—Ä–æ–º—ã—à–ª–µ–Ω–Ω—ã–µ —Ç–µ—Ä–º–∏–Ω—ã
    # –ù–æ —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –µ—Å—Ç—å —Å–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã–µ –¥–ª—è –º–∞—à–∏–Ω–æ—Å—Ç—Ä–æ–µ–Ω–∏—è —Å–ª–æ–≤–∞
    if '–¥—Ä—É–≥–∏–µ_–ø—Ä–æ–º—ã—à–ª–µ–Ω–Ω—ã–µ' in INDUSTRY_SEGMENTS_KEYWORDS:
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Å–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã—Ö —Ç–µ—Ä–º–∏–Ω–æ–≤ –º–∞—à–∏–Ω–æ—Å—Ç—Ä–æ–µ–Ω–∏—è
        machinery_specific = [
            '—Å—Ç–∞–Ω–æ—á–Ω–∏–∫', '—Ç–æ–∫–∞—Ä', '—Ñ—Ä–µ–∑–µ—Ä–æ–≤', '—Å–≤–µ—Ä–ª', '—à–ª–∏—Ñ–æ–≤', '—Ä–∞—Å—Ç–æ—á–Ω',
            '–º–µ—Ö–∞–Ω–æ—Å–±–æ—Ä–æ—á', '—Å–±–æ—Ä—â–∏–∫', '–Ω–∞–ª–∞–¥—á–∏–∫', '—Ä–µ–º–æ–Ω—Ç–Ω–∏–∫', '–º–µ—Ö–∞–Ω–∏–∫',
            '–∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä', '—á–µ—Ä—Ç–µ–∂–Ω–∏–∫', '–∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç', '–ø—Ä–µ—Å—Å', '—à—Ç–∞–º–ø–æ–≤',
            '–≥–∏–¥—Ä–∞–≤–ª–∏–∫', '–ø–Ω–µ–≤–º–∞—Ç–∏–∫', '–∫–æ–º–ø—Ä–µ—Å—Å–æ—Ä', '—Ç—É—Ä–±–∏–Ω', '–¥–≤–∏–≥–∞—Ç–µ–ª'
        ]
        
        has_machinery_term = any(term in combined_text for term in machinery_specific)
        
        # –ï—Å–ª–∏ –µ—Å—Ç—å —Å–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã–µ —Ç–µ—Ä–º–∏–Ω—ã –º–∞—à–∏–Ω–æ—Å—Ç—Ä–æ–µ–Ω–∏—è –ò –æ–±—â–∏–µ –ø—Ä–æ–º—ã—à–ª–µ–Ω–Ω—ã–µ
        if has_machinery_term:
            for keyword in INDUSTRY_SEGMENTS_KEYWORDS['–¥—Ä—É–≥–∏–µ_–ø—Ä–æ–º—ã—à–ª–µ–Ω–Ω—ã–µ']:
                keyword_clean = keyword.strip()
                if f' {keyword_clean} ' in f' {combined_text} ' or keyword_clean in combined_text:
                    return '–º–∞—à–∏–Ω–æ—Å—Ç—Ä–æ–µ–Ω–∏–µ'
    
    return '–¥—Ä—É–≥–æ–µ'


def classify_position_level(vacancy_name: str) -> str:
    """
    –ö–ª–∞—Å—Å–∏—Ñ–∏—Ü–∏—Ä—É–µ—Ç –≤–∞–∫–∞–Ω—Å–∏—é –ø–æ —É—Ä–æ–≤–Ω—é –ø–æ–∑–∏—Ü–∏–∏.
    –£–ª—É—á—à–µ–Ω–Ω–∞—è –≤–µ—Ä—Å–∏—è —Å –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–∞–º–∏ - —Å–Ω–∞—á–∞–ª–∞ –ø—Ä–æ–≤–µ—Ä—è–µ–º –±–æ–ª–µ–µ —Å–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏.
    
    Args:
        vacancy_name: –ù–∞–∑–≤–∞–Ω–∏–µ –≤–∞–∫–∞–Ω—Å–∏–∏
        
    Returns:
        –£—Ä–æ–≤–µ–Ω—å –ø–æ–∑–∏—Ü–∏–∏ –∏–ª–∏ '–¥—Ä—É–≥–æ–µ'
    """
    name_lower = (vacancy_name or '').lower()
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –≤ –ø–æ—Ä—è–¥–∫–µ –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–∞ (–æ—Ç –±–æ–ª–µ–µ —Å–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã—Ö –∫ –º–µ–Ω–µ–µ —Å–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã–º)
    # –≠—Ç–æ –≤–∞–∂–Ω–æ, —á—Ç–æ–±—ã "–º–∞—Å—Ç–µ—Ä —É—á–∞—Å—Ç–∫–∞" –ø–æ–ø–∞–ª –≤ —Ä—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª—å, –∞ –Ω–µ –≤ —Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç
    priority_order = [
        '–≤—ã—Å—à–µ–µ_—Ä—É–∫–æ–≤–æ–¥—Å—Ç–≤–æ',
        '—Ä—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª—å', 
        '–∏–Ω–∂–µ–Ω–µ—Ä',
        '—Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç',
        '—Ä–∞–±–æ—á–∏–π'
    ]
    
    # –°–Ω–∞—á–∞–ª–∞ –ø—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –≤ –ø–æ—Ä—è–¥–∫–µ –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–∞
    for level in priority_order:
        if level in POSITION_LEVELS_KEYWORDS:
            for keyword in POSITION_LEVELS_KEYWORDS[level]:
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å–ª–æ–≤–∞ (–∫–∞–∫ –æ—Ç–¥–µ–ª—å–Ω–æ–µ —Å–ª–æ–≤–æ)
                keyword_clean = keyword.strip()
                if f' {keyword_clean} ' in f' {name_lower} ':
                    return level
                elif keyword_clean in name_lower:
                    # –î–ª—è –Ω–µ–∫–æ—Ç–æ—Ä—ã—Ö –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤ –ø—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç
                    # –ù–∞–ø—Ä–∏–º–µ—Ä, "–º–∞—Å—Ç–µ—Ä —É—á–∞—Å—Ç–∫–∞" –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å —Ä—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª–µ–º, –∞ –Ω–µ —Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç–æ–º
                    if level == '—Ä—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª—å' and keyword_clean == '–º–∞—Å—Ç–µ—Ä':
                        if '—É—á–∞—Å—Ç–∫–∞' in name_lower or '—É—á–∞—Å—Ç–æ–∫' in name_lower:
                            return level
                    return level
    
    return '–¥—Ä—É–≥–æ–µ'


